    /**
     * Reject generic production / mood tags unless the asset family plausibly supports them.
     */
    protected function visionTagGenericFillerRejectionReason(string $c, ?string $family): ?string
    {
        $family ??= 'unknown';
        foreach (['lighting setup', 'studio lighting', 'product photography'] as $p) {
            if (str_contains($c, $p)) {
                return 'global_production_filler';
            }
        }
        if (str_contains($c, 'photoshoot') || str_contains($c, 'photo shoot')) {
            return 'global_production_filler';
        }
        foreach (['professional', 'modern', 'clean', 'aesthetic', 'branding'] as $w) {
            if ($c === $w) {
                return 'generic_one_word';
            }
        }
        if (preg_match('/\bbackground\b/u', $c) === 1 && ! $this->visionTagBackgroundPhraseIsAllowed($c)) {
            return 'generic_background';
        }

        $peopleish = ['lifestyle_photo', 'people_photo'];
        $photoScene = ['lifestyle_photo', 'people_photo', 'environment_photo'];
        $productish = ['product_image', 'product_render'];

        if (preg_match('/\bstudio\b/u', $c) === 1 && ! in_array($family, $peopleish, true)) {
            return 'generic_studio';
        }
        if (preg_match('/\bmodel\b/u', $c) === 1 && ! in_array($family, $peopleish, true)) {
            return 'generic_model';
        }
        if (preg_match('/\bfashion\b/u', $c) === 1 && ! in_array($family, $peopleish, true)) {
            return 'generic_fashion';
        }
        if (preg_match('/\blifestyle\b/u', $c) === 1 && ! in_array($family, $peopleish, true)) {
            return 'generic_lifestyle_word';
        }

        $allowIndoorOutdoor = in_array($family, array_merge($photoScene, $productish), true);
        if (! $allowIndoorOutdoor && (preg_match('/\bindoor\b/u', $c) === 1 || preg_match('/\boutdoor\b/u', $c) === 1)) {
            return 'generic_indoor_outdoor';
        }

        $allowClose = in_array($family, array_merge($peopleish, $productish), true);
        if (! $allowClose && (str_contains($c, 'close up') || str_contains($c, 'closeup'))) {
            return 'generic_close_up';
        }

        return null;
    }

    /**
     * Merged vision-tag ban set: normalized category name/slug (plus simple singular/plural variants) plus
     * aliases from {@see visionTagCategoryAliasBanConfigRows} rows whose `match` rules fit this category.
     *
     * @return array<string, true>
     */
    protected function buildVisionTagCategoryBanSet(?Category $category): array
    {
        if ($category === null) {
            return [];
        }

        $set = [];

        foreach ($this->normalizedCategoryPhraseBanSet($category) as $phrase => $_) {
            if ($phrase !== '') {
                $set[$phrase] = true;
            }
        }

        foreach ($this->visionTagCategoryAliasBanConfigRows() as $row) {
            if (! is_array($row) || ! $this->categoryMatchesVisionTagAliasBanRow($row, $category)) {
                continue;
            }
            $aliases = $row['aliases'] ?? [];
            if (! is_array($aliases)) {
                continue;
            }
            foreach ($aliases as $alias) {
                if (! is_string($alias) || trim($alias) === '') {
                    continue;
                }
                $c = trim($this->applyVisionTagPhraseAliases($this->canonicalizeAiVisionTagString($alias)));
                if ($c !== '') {
                    $set[$c] = true;
                }
            }
        }

        return $set;
    }

    /**
     * @return list<array<string, mixed>>
     */
    protected function visionTagCategoryAliasBanConfigRows(): array
    {
        $base = config('ai.metadata_tagging.vision_tag_category_alias_bans', []);
        $append = config('ai.metadata_tagging.vision_tag_category_alias_bans_append', []);

        return array_merge(
            is_array($base) ? $base : [],
            is_array($append) ? $append : []
        );
    }

    protected function categoryRegexMatches(string $pattern, string $subject): bool
    {
        if ($pattern === '') {
            return false;
        }
        $ok = @preg_match($pattern, $subject);
        if ($ok === false) {
            Log::warning('[AiMetadataGenerationService] Invalid vision_tag_category_alias_bans regex', [
                'pattern' => $pattern,
            ]);

            return false;
        }

        return $ok === 1;
    }

    protected function categoryMatchesVisionTagAliasBanRow(array $row, Category $category): bool
    {
        $match = $row['match'] ?? null;
        if (! is_array($match)) {
            return false;
        }

        $name = (string) $category->name;
        $slug = strtolower(trim(str_replace(['_', '-'], ' ', (string) ($category->slug ?? ''))));

        foreach ($match['name_not_regex_any'] ?? [] as $rx) {
            if (is_string($rx) && $rx !== '' && $this->categoryRegexMatches($rx, $name)) {
                return false;
            }
        }
        foreach ($match['slug_not_regex_any'] ?? [] as $rx) {
            if (is_string($rx) && $rx !== '' && $this->categoryRegexMatches($rx, $slug)) {
                return false;
            }
        }

        foreach ($match['slug_equals_any'] ?? [] as $want) {
            if (! is_string($want) || $want === '') {
                continue;
            }
            if ($slug === strtolower(trim(str_replace(['_', '-'], ' ', $want)))) {
                return true;
            }
        }

        foreach ($match['name_regex_any'] ?? [] as $rx) {
            if (is_string($rx) && $rx !== '' && $this->categoryRegexMatches($rx, $name)) {
                return true;
            }
        }

        foreach ($match['slug_regex_any'] ?? [] as $rx) {
            if (is_string($rx) && $rx !== '' && $this->categoryRegexMatches($rx, $slug)) {
                return true;
            }
        }

        return false;
    }

    /**
     * Normalized category name and slug phrases (plus simple singular/plural) — reject as tags when identical.
     *
     * @return array<string, true>
     */
    protected function normalizedCategoryPhraseBanSet(Category $category): array
    {
        $set = [];
        $sources = [];
        if (is_string($category->name) && trim($category->name) !== '') {
            $sources[] = $category->name;
        }
        $slug = $category->slug ?? null;
        if (is_string($slug) && trim($slug) !== '') {
            $sources[] = str_replace(['-', '_'], ' ', $slug);
        }
        foreach ($sources as $src) {
            $base = $this->canonicalizeAiVisionTagString($src);
            foreach ($this->simpleEnglishPluralSingularPhraseVariants($base) as $v) {
                if ($v !== '') {
                    $set[$v] = true;
                }
            }
        }

        return $set;
    }

    /**
     * @return list<string>
     */
    protected function simpleEnglishPluralSingularPhraseVariants(string $normalizedPhrase): array
    {
        $normalizedPhrase = trim(preg_replace('/\s+/', ' ', $normalizedPhrase));
        if ($normalizedPhrase === '') {
            return [];
        }
        $variants = [$normalizedPhrase];
        if (strlen($normalizedPhrase) >= 3 && substr($normalizedPhrase, -1) === 's' && substr($normalizedPhrase, -2, 1) !== 's') {
            $variants[] = substr($normalizedPhrase, 0, -1);
        } elseif (substr($normalizedPhrase, -1) !== 's') {
            $variants[] = $normalizedPhrase.'s';
        }

        return array_values(array_unique(array_filter($variants)));
    }

    protected function visionTagCategoryRestatementRejectionReason(string $canonicalLower, array $categoryVisionBanSet): ?string
    {
        if (isset($categoryVisionBanSet[$canonicalLower])) {
            return 'category_restatement';
        }

        return null;
    }

    /**
     * @return string|null rejection reason code, or null if passed sanitizer gates (blocklist handled separately)
     */
    protected function getVisionTagSanitizerRejectionReason(
        string $canonicalLower,
        ?string $detectedAssetType,
        array $categoryVisionBanSet = []
    ): ?string {
        if ($canonicalLower === '') {
            return 'empty_after_normalize';
        }
        if ($this->visionTagWordCount($canonicalLower) > self::VISION_TAG_MAX_WORDS) {
            return 'too_many_words';
        }
        if ($this->visionTagLooksLikePromptLeakage($canonicalLower)) {
            return 'prompt_leakage';
        }
        if (($prov = $this->visionTagProvenancePipelineLeakageRejectionReason($canonicalLower)) !== null) {
            return $prov;
        }
        if (($catReason = $this->visionTagCategoryRestatementRejectionReason($canonicalLower, $categoryVisionBanSet)) !== null) {
            return $catReason;
        }
        $filler = $this->visionTagGenericFillerRejectionReason($canonicalLower, $detectedAssetType);
        if ($filler !== null) {
            return $filler;
        }

        return null;
    }

    /**
     * @param  array<string, int|float>  $tagParseStats
     * @param  array<string, true>  $seenCanonicalTags
     * @param  list<array{value: string, confidence: float}>  $tags
     */
    protected function acceptVisionTagCandidate(
        string $assetId,
        string $tagValue,
        mixed $confidence,
        float $minConf,
        array &$tags,
        array &$tagParseStats,
        ?string $detectedAssetType,
        array &$seenCanonicalTags,
        array $categoryVisionBanSet = []
    ): void {
        $tagParseStats['raw_tag_count']++;

        if (! is_numeric($confidence)) {
            $tagParseStats['rejected_low_confidence_count']++;

            return;
        }

        if ((float) $confidence < $minConf) {
            Log::debug('[AiMetadataGenerationService] Low confidence tag skipped', [
                'asset_id' => $assetId,
                'tag' => $tagValue,
                'confidence' => $confidence,
                'min_confidence' => $minConf,
            ]);
            $tagParseStats['rejected_low_confidence_count']++;

            return;
        }

        $canonicalTag = $this->canonicalizeAiVisionTagString($tagValue);
        $canonicalTag = $this->applyVisionTagPhraseAliases($canonicalTag);
        $sanitizerReason = $this->getVisionTagSanitizerRejectionReason($canonicalTag, $detectedAssetType, $categoryVisionBanSet);
        if ($sanitizerReason !== null) {
            $tagParseStats['rejected_sanitizer_count'] = (int) ($tagParseStats['rejected_sanitizer_count'] ?? 0) + 1;
            Log::debug('[AiMetadataGenerationService] Vision tag sanitizer rejected', [
                'asset_id' => $assetId,
                'reason' => $sanitizerReason,
                'original' => $tagValue,
                'normalized' => $canonicalTag,
                'detected_asset_type' => $detectedAssetType,
            ]);

            return;
        }

        if ($this->isVisionTagBlocklisted($canonicalTag)) {
            $tagParseStats['rejected_blocklist_count'] = (int) ($tagParseStats['rejected_blocklist_count'] ?? 0) + 1;
            Log::debug('[AiMetadataGenerationService] Vision tag sanitizer rejected', [
                'asset_id' => $assetId,
                'reason' => 'blocklist',
                'original' => $tagValue,
                'normalized' => $canonicalTag,
                'detected_asset_type' => $detectedAssetType,
            ]);

            return;
        }

        if ($this->isRedundantTag($canonicalTag)) {
            Log::debug('[AiMetadataGenerationService] Vision tag sanitizer rejected', [
                'asset_id' => $assetId,
                'reason' => 'redundant_photoshoot',
                'original' => $tagValue,
                'normalized' => $canonicalTag,
                'detected_asset_type' => $detectedAssetType,
            ]);

            return;
        }

        if (isset($seenCanonicalTags[$canonicalTag])) {
            $tagParseStats['rejected_duplicate_count'] = (int) ($tagParseStats['rejected_duplicate_count'] ?? 0) + 1;
            Log::debug('[AiMetadataGenerationService] Vision tag sanitizer rejected', [
                'asset_id' => $assetId,
                'reason' => 'duplicate',
                'original' => $tagValue,
                'normalized' => $canonicalTag,
                'detected_asset_type' => $detectedAssetType,
            ]);

            return;
        }

        Log::debug('[AiMetadataGenerationService] Vision tag sanitizer accepted', [
            'asset_id' => $assetId,
            'original' => $tagValue,
            'normalized' => $canonicalTag,
            'confidence' => (float) $confidence,
            'detected_asset_type' => $detectedAssetType,
        ]);

        $tagParseStats['passed_confidence_count']++;
        $seenCanonicalTags[$canonicalTag] = true;
        $tags[] = [
            'value' => $canonicalTag,
            'confidence' => (float) $confidence,
        ];
    }

    /**
     * When the primary "tags" array is empty, accept common alternate shapes from vision models.
     *
     * @param  array<string, mixed>  $data
     * @param  array<string, int|float>  $tagParseStats
     * @param  array<string, true>  $seenCanonicalTags
     * @param  list<array{value: string, confidence: float}>  $tags
     */
    protected function parseTagsFromAlternateVisionJsonShapes(
        array $data,
        float $minConf,
        string $assetId,
        array &$tags,
        array &$tagParseStats,
        ?string $detectedAssetType,
        array &$seenCanonicalTags,
        array $categoryVisionBanSet = []
    ): void {
        $rows = [];

        $tagsNode = $data['tags'] ?? null;
        if (is_array($tagsNode) && $tagsNode !== [] && $this->isAssociativeArray($tagsNode)) {
            foreach ($tagsNode as $value => $confidence) {
                if (is_string($value) && $value !== '' && is_numeric($confidence)) {
                    $rows[] = ['value' => $value, 'confidence' => (float) $confidence];
                }
            }
        }

        foreach (['keywords', 'suggested_tags', 'descriptive_tags', 'image_tags', 'search_tags'] as $altKey) {
            $block = $data[$altKey] ?? null;
            if (! is_array($block)) {
                continue;
            }
            foreach ($block as $item) {
                if (is_string($item) && $item !== '') {
                    $rows[] = ['value' => $item, 'confidence' => $minConf];

                    continue;
                }
                if (! is_array($item)) {
                    continue;
                }
                $v = $item['value'] ?? $item['tag'] ?? null;
                $c = $item['confidence'] ?? null;
                if (is_string($v) && $v !== '') {
                    $rows[] = [
                        'value' => $v,
                        'confidence' => is_numeric($c) ? (float) $c : $minConf,
                    ];
                }
            }
        }

        foreach ($rows as $row) {
            $this->acceptVisionTagCandidate(
                $assetId,
                $row['value'],
                $row['confidence'],
                $minConf,
                $tags,
                $tagParseStats,
                $detectedAssetType,
                $seenCanonicalTags,
                $categoryVisionBanSet
            );
        }
    }

    protected function isAssociativeArray(array $arr): bool
    {
        if ($arr === []) {
            return false;
        }

        return array_keys($arr) !== range(0, count($arr) - 1);
    }

    /**
     * Legacy alias for vision-tag canonicalization ({@see canonicalizeAiVisionTagString}).
     *
     * @return string Normalized tag
     */
    protected function normalizeTag(string $tag): string
    {
        return $this->canonicalizeAiVisionTagString($tag);
    }

    /**
     * @return list<string> lowercase normalized single-tag strings to drop (see config ai.metadata_tagging.vision_tag_blocklist).
     */
    protected function visionTagBlocklist(): array
    {
        $raw = config('ai.metadata_tagging.vision_tag_blocklist', ['model', 'fashion']);
        if (! is_array($raw)) {
            return ['model', 'fashion'];
        }
        $out = [];
        foreach ($raw as $t) {
            if (is_string($t) && $t !== '') {
                $out[] = strtolower(trim($t));
            }
        }

        return array_values(array_unique($out));
    }

    protected function isVisionTagBlocklisted(string $normalizedTag): bool
    {
        return in_array($normalizedTag, $this->visionTagBlocklist(), true);
    }

    /**
     * Tags that duplicate category-level concepts and should never be stored (e.g. "photo shoot" in a DAM photography context).
     */
    protected function isRedundantTag(string $normalized): bool
    {
        $t = preg_replace('/\s+/', ' ', trim($normalized));
        $compact = str_replace(' ', '', $t);

        return $t === 'photo shoot' || strcasecmp($compact, 'photoshoot') === 0;
    }

    /**
     * Check if value is allowed (in field options).
     *
     * @param  mixed  $value
     * @param  array  $field  Field definition
     */
    protected function isValueAllowed($value, array $field): bool
    {
        $options = $field['options'] ?? [];

        if ($field['type'] === 'multiselect') {
            if (! is_array($value)) {
                return false;
            }
            // All values must be in options
            foreach ($value as $v) {
                if (! in_array($v, $options, true)) {
                    return false;
                }
            }

            return true;
        }

        // Single select
        return in_array($value, $options, true);
    }

    /**
     * Create metadata field candidates in asset_metadata_candidates table.
     *
     * Stores AI-generated field values as candidates (not approved data).
     * Candidates must be reviewed and approved before being applied to the asset.
     *
     * @param  array  $candidates  Array of field candidates: ['field_key' => ['value' => mixed, 'confidence' => float]]
     * @return int Number of candidates created
     */
    protected function createCandidates(Asset $asset, array $candidates): int
    {
        if (empty($candidates)) {
            return 0;
        }

        $created = 0;
        foreach ($candidates as $fieldKey => $candidateData) {
            // Get field ID
            $field = DB::table('metadata_fields')
                ->where('key', $fieldKey)
                ->where(function ($query) use ($asset) {
                    $query->where('scope', 'system')
                        ->orWhere(function ($q) use ($asset) {
                            $q->where('tenant_id', $asset->tenant_id)
                                ->where('scope', '!=', 'system');
                        });
                })
                ->first();

            if (! $field) {
                continue;
            }

            // Check if candidate already exists
            $existing = DB::table('asset_metadata_candidates')
                ->where('asset_id', $asset->id)
                ->where('metadata_field_id', $field->id)
                ->where('producer', 'ai')
                ->whereNull('resolved_at')
                ->whereNull('dismissed_at')
                ->first();

            if ($existing) {
                // Update existing candidate
                DB::table('asset_metadata_candidates')
                    ->where('id', $existing->id)
                    ->update([
                        'value_json' => json_encode($candidateData['value']),
                        'confidence' => $candidateData['confidence'],
                        'source' => 'ai',
                        'updated_at' => now(),
                    ]);
            } else {
                // Create new candidate
                DB::table('asset_metadata_candidates')->insert([
                    'asset_id' => $asset->id,
                    'metadata_field_id' => $field->id,
                    'value_json' => json_encode($candidateData['value']),
                    'confidence' => $candidateData['confidence'],
                    'source' => 'ai',
                    'producer' => 'ai',
                    'created_at' => now(),
                    'updated_at' => now(),
                ]);
            }

            $created++;
        }

        return $created;
    }

    /**
     * Canonical tag strings already present on the asset ({@see asset_tags}), for deduping AI candidates.
     *
     * @return array<string, true>
     */
    protected function existingCanonicalTagsOnAsset(Asset $asset, ?Tenant $tenant): array
    {
        if (! $tenant) {
            return [];
        }

        $normalizer = app(TagNormalizationService::class);
        $out = [];
        foreach (DB::table('asset_tags')->where('asset_id', $asset->id)->pluck('tag') as $raw) {
            $c = $normalizer->normalize($raw, $tenant);
            if ($c !== null) {
                $out[$c] = true;
            }
        }

        return $out;
    }

    /**
     * Create tag candidates in asset_tag_candidates table.
     *
     * Stores AI-generated tags as candidates (not approved data).
     * Tags must be reviewed and approved before being applied to the asset.
     *
     * @param  array  $tags  Array of ['value' => string, 'confidence' => float]
     * @return int Number of tag candidates created
     */
    protected function createTags(Asset $asset, array $tags): int
    {
        if (empty($tags)) {
            return 0;
        }

        $tenant = Tenant::find($asset->tenant_id);
        $tagNormalizationService = app(TagNormalizationService::class);
        $existingCanonicalOnAsset = $this->existingCanonicalTagsOnAsset($asset, $tenant);

        $created = 0;
        $now = now();

        foreach ($tags as $tagData) {
            $tagValue = $tagData['value'] ?? null;
            $confidence = $tagData['confidence'] ?? null;

            if (empty($tagValue) || ! is_string($tagValue)) {
                continue;
            }

            // Phase J.2.1: Normalize tag and check if canonical form has been dismissed
            $canonicalTag = $tagNormalizationService->normalize($tagValue, $tenant);

            // Skip if normalization results in blocked/invalid tag
            if ($canonicalTag === null) {
                continue;
            }

            // User (or auto-apply) already has this tag on the asset — skip redundant candidate
            if (isset($existingCanonicalOnAsset[$canonicalTag])) {
                continue;
            }

            // Check if any candidate with this canonical form has been dismissed
            $allCandidates = DB::table('asset_tag_candidates')
                ->where('asset_id', $asset->id)
                ->where('producer', 'ai')
                ->whereNotNull('dismissed_at')
                ->get();

            $canonicalDismissed = false;
            foreach ($allCandidates as $dismissedCandidate) {
                $dismissedCanonical = $tagNormalizationService->normalize($dismissedCandidate->tag, $tenant);
                if ($dismissedCanonical === $canonicalTag) {
                    $canonicalDismissed = true;
                    break;
                }
            }

            if ($canonicalDismissed) {
                // Skip creating candidate for dismissed canonical tag
                continue;
            }

            // Check if candidate already exists (avoid duplicates)
            $existing = DB::table('asset_tag_candidates')
                ->where('asset_id', $asset->id)
                ->where('tag', $tagValue)
                ->where('producer', 'ai')
                ->whereNull('resolved_at')
                ->whereNull('dismissed_at')
                ->first();

            if ($existing) {
                // Update existing candidate with new confidence if higher
                if (is_numeric($confidence) && $confidence > ($existing->confidence ?? 0)) {
                    DB::table('asset_tag_candidates')
                        ->where('id', $existing->id)
                        ->update([
                            'confidence' => $confidence,
                            'updated_at' => $now,
                        ]);
                    $created++;
                }

                continue;
            }

            // Create new tag candidate (NOT approved - stored as candidate for review)
            DB::table('asset_tag_candidates')->insert([
                'asset_id' => $asset->id,
                'tag' => $tagValue,
                'source' => 'ai',
                'confidence' => is_numeric($confidence) ? $confidence : null,
                'producer' => 'ai',
                'created_at' => $now,
                'updated_at' => $now,
            ]);

            $existingCanonicalOnAsset[$canonicalTag] = true;
            $created++;
        }

        return $created;
    }
