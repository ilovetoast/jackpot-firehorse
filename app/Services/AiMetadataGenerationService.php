    /**
     * Parse AI provider response into candidates and tags.
     *
     * Handles unified response with both 'fields' and 'tags' sections from ONE Vision API call.
     *
     * @param  array  $response  Provider response (from AIProviderInterface::analyzeImage)
     * @param  array  $fields  Field definitions
     * @return array ['candidates' => array, 'tags' => array] Candidates and tags ready for database insertion
     */
    protected function parseResponse(array $response, Asset $asset, array $fields): array
    {
        $text = $response['text'] ?? '';
        $candidates = [];
        $tags = [];
        $minConf = $this->getTaggingMinConfidence();
        $tagParseStats = [
            'raw_tag_count' => 0,
            'passed_confidence_count' => 0,
            'rejected_low_confidence_count' => 0,
            'rejected_blocklist_count' => 0,
            'rejected_sanitizer_count' => 0,
            'rejected_duplicate_count' => 0,
        ];

        try {
            $data = json_decode($text, true);
            if (! is_array($data)) {
                Log::warning('[AiMetadataGenerationService] Invalid JSON response', [
                    'asset_id' => $asset->id,
                    'response' => $text,
                ]);

                return ['candidates' => [], 'tags' => [], 'tag_parse_stats' => $tagParseStats];
            }

            $detectedAssetType = $this->parseDetectedAssetTypeFromVisionJson($data);
            $seenCanonicalTags = [];

            $category = null;
            $catIdForParse = $asset->metadata['category_id'] ?? null;
            if ($catIdForParse !== null && $catIdForParse !== '') {
                $category = Category::find($catIdForParse);
            }
            $categoryVisionBanSet = $this->buildVisionTagCategoryBanSet($category);

            // Parse structured fields
            $fieldsData = $data['fields'] ?? [];
            if (is_array($fieldsData)) {
                foreach ($fieldsData as $fieldKey => $fieldData) {
                    if (! is_array($fieldData)) {
                        continue;
                    }

                    $value = $fieldData['value'] ?? null;
                    $confidence = $fieldData['confidence'] ?? null;

                    if (! is_numeric($confidence) || (float) $confidence < $minConf) {
                        Log::debug('[AiMetadataGenerationService] Low confidence field skipped', [
                            'asset_id' => $asset->id,
                            'field_key' => $fieldKey,
                            'confidence' => $confidence,
                            'min_confidence' => $minConf,
                        ]);

                        continue;
                    }

                    // Find field definition
                    $field = null;
                    foreach ($fields as $f) {
                        if ($f['key'] === $fieldKey) {
                            $field = $f;
                            break;
                        }
                    }

                    if (! $field) {
                        continue;
                    }

                    // Validate value is in allowed options
                    if (! $this->isValueAllowed($value, $field)) {
                        Log::debug('[AiMetadataGenerationService] Invalid value skipped', [
                            'asset_id' => $asset->id,
                            'field_key' => $fieldKey,
                            'value' => $value,
                        ]);

                        continue;
                    }

                    $candidates[$fieldKey] = [
                        'value' => $value,
                        'confidence' => (float) $confidence,
                    ];
                }
            }

            // Parse general tags (primary shape: list of {value, confidence})
            $tagsData = $data['tags'] ?? [];
            if (is_array($tagsData)) {
                foreach ($tagsData as $tagData) {
                    if (! is_array($tagData)) {
                        if (is_string($tagData)) {
                            $tagData = ['value' => $tagData, 'confidence' => $minConf];
                        } else {
                            continue;
                        }
                    }

                    $tagValue = $tagData['value'] ?? null;
                    $confidence = $tagData['confidence'] ?? null;
                    if (empty($tagValue) || ! is_string($tagValue)) {
                        continue;
                    }

                    $this->acceptVisionTagCandidate(
                        $asset->id,
                        $tagValue,
                        $confidence,
                        $minConf,
                        $tags,
                        $tagParseStats,
                        $detectedAssetType,
                        $seenCanonicalTags,
                        $categoryVisionBanSet
                    );
                }
            }

            // Models often return [] or omit tags under strict confidence rules, or use alternate JSON keys / a map.
            if (($tagParseStats['raw_tag_count'] ?? 0) === 0) {
                $this->parseTagsFromAlternateVisionJsonShapes(
                    $data,
                    $minConf,
                    $asset->id,
                    $tags,
                    $tagParseStats,
                    $detectedAssetType,
                    $seenCanonicalTags,
                    $categoryVisionBanSet
                );
            }
        } catch (\Exception $e) {
            Log::error('[AiMetadataGenerationService] Failed to parse response', [
                'asset_id' => $asset->id,
                'error' => $e->getMessage(),
                'response' => $text,
            ]);
        }

        return [
            'candidates' => $candidates,
            'tags' => $tags,
            'tag_parse_stats' => $tagParseStats,
        ];
    }

    /**
     * Optional vision JSON key: silent asset classification (not persisted).
     */
    protected function parseDetectedAssetTypeFromVisionJson(array $data): ?string
    {
        if (! array_key_exists('detected_asset_type', $data)) {
            return null;
        }
        $v = $data['detected_asset_type'];
        if (! is_string($v)) {
            return null;
        }
        $v = strtolower(trim($v));
        if ($v === '' || $v === 'unknown') {
            return null;
        }
        if (in_array($v, self::VISION_DETECTED_ASSET_TYPES, true)) {
            return $v;
        }
        $mapped = $this->mapLegacyVisionDetectedAssetType($v);

        return in_array($mapped, self::VISION_DETECTED_ASSET_TYPES, true) ? $mapped : null;
    }

    /**
     * Map older vision JSON classifications to current asset-family ids.
     */
    protected function mapLegacyVisionDetectedAssetType(string $v): ?string
    {
        return match ($v) {
            'ui_screenshot' => 'screen_capture',
            'product_photo' => 'product_image',
            'packaging' => 'product_image',
            'logo_graphic' => 'brand_mark',
            'illustration' => 'graphic_design',
            'printed_layout' => 'marketing_layout',
            default => null,
        };
    }

    /**
     * Normalize only generic structural variants (after {@see canonicalizeAiVisionTagString}).
     * Do not collapse product-specific compounds (e.g. bourbon bottle, bottle label) — those stay searchable.
     */
    protected function applyVisionTagPhraseAliases(string $canonical): string
    {
        $t = preg_replace('/\s+/', ' ', trim($canonical));
        if ($t === '') {
            return '';
        }
        $pairs = [
            'bourbon whiskey' => 'bourbon',
            'bottle product' => 'bottle',
            'product bottle' => 'bottle',
            'transparent bg' => 'transparent background',
            'ui screenshot' => 'screen capture',
            'app screen' => 'screen capture',
        ];
        foreach ($pairs as $from => $to) {
            if (str_contains($t, $from)) {
                $t = str_replace($from, $to, $t);
            }
        }
        $t = preg_replace('/\s+/', ' ', trim($t));
        if ($t === 'webpage') {
            $t = 'screen capture';
        }

        return $t;
    }

    /**
     * Canonical form for vision tag comparison and storage (hyphens/underscores → spaces, punctuation stripped).
     */
    protected function canonicalizeAiVisionTagString(string $tag): string
    {
        $tag = strtolower(trim($tag));
        $tag = str_replace(['_', '-'], ' ', $tag);
        $tag = preg_replace('/[^a-z0-9\s]+/', '', $tag);
        $tag = preg_replace('/\s+/', ' ', $tag);
        $tag = trim((string) $tag);
        if (strlen($tag) > 1 && substr($tag, -1) === 's' && substr($tag, -2, 1) !== 's') {
            $tag = substr($tag, 0, -1);
        }
        $tag = preg_replace('/\s+/', ' ', trim($tag));

        return $tag;
    }

    protected function visionTagWordCount(string $canonicalLower): int
    {
        if ($canonicalLower === '') {
            return 0;
        }

        return count(preg_split('/\s+/', $canonicalLower, -1, PREG_SPLIT_NO_EMPTY));
    }

